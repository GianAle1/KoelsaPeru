    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

// Obtener los valores de los campos de texto
        String usuario = jTextField1.getText();
        String contrasena = new String(jTextField2.getPassword());
        // Crear un objeto Usuario
        Usuario usuarioObj = new Usuario(usuario, contrasena);
        // Llamar al controlador para validar las credenciales
        LoginController controlador = new LoginController();
        boolean loginExitoso = controlador.validarLogin(usuarioObj);
        if (loginExitoso) {
            JOptionPane.showMessageDialog(this, "Login exitoso!", "Éxito", JOptionPane.INFORMATION_MESSAGE);
            // Aquí puedes redirigir a la siguiente ventana si el login fue exitoso.
            
            MenuView menuView = new MenuView();
            menuView.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(this, "Usuario o contraseña incorrectos.", "Error", JOptionPane.ERROR_MESSAGE);
        }       
    }//GEN-LAST:event_jButton1ActionPerformed

